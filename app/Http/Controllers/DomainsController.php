     * Store a newly created resource in storage.
     *
     * @param  \App\Http\Requests\StoreDomainRequest  $request
     * @return \Illuminate\Http\Response
     */
    public function store(StoreDomainRequest $request)
    {
        $data = [
            'name' => $request->name,
            'registration_date' => Carbon::now()->format('Y-m-d'),
            'expiration_date' => Carbon::now()->addYear()->format('Y-m-d'),
            'user_id' => Auth::id(),
        ];

        $domain = Domain::create($data);

        // Create order and items
        $order = Order::create(['user_id' => \Auth::id()]);
        $price = env('DOMAIN_PRICE');

        $orderItem = OrderItem::create([
            'order_id' => $order->id,
            'itemable_type' => Domain::class,
            'itemable_id' => $domain->id,
            'amount' => $price
        ]);

        return redirect(route ('nameservers.create', [$domain]));
    }

